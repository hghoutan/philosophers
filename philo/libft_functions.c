#include "philo.h"

int	ft_atoi(const char *str)
{
	int	ng;
	int	i;
	int	num;

	ng = 1;
	i = 0;
	num = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			ng = -1;
		str++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	return (num * ng);
}